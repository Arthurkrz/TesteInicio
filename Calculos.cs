using System;
using System.Collections.Generic;
using System.Text;

namespace Tests
{
    public class Calculos
    {
        public int Soma(int n1, int n2)
        {
            return n1 + n2;
        }
        public int Subtracao(int n1, int n2)
        {
            return n1 - n2;
        }
        public int Multiplicacao(int n1, int n2)
        {
            return n1 * n2;
        }
        public double Divisao(double n1, double n2)
        {
            return n1 / n2;
        }
    }
}
